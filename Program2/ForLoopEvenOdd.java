package Program2;

public class ForLoopEvenOdd
{
    public static void main(String[] args)
    {
        System.out.println("The 10 Even Numbers");
        for(int k  = 1 ; k <= 10 ; k++)
        {
            System.out.println(2 * k);
        }

        System.out.println("The 10 Odd Numbers");
        for(int j = 1; j <= 10; j++)
        {
            System.out.println(2 * j -1);
        }


    }
}
