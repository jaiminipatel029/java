package program4;

public class WhileLoopEvenOdd
{
    public static void main(String[] args) {


        System.out.println("The 10 Even Numbers");
        int i = 1;
        while (i <= 10) {
            System.out.println(2 * i);
            i++;

        }

        System.out.println("The 10 Odd Numbers");
        int j = 1;
            while (j <= 10) {
                System.out.println(2 * j - 1);
                j++;

            }


        }
}

